    // bool isEntranceList,
    List housList,
    List entranceList,
    bool isBottomSheet02,
    bool isBottomSheet03,
    bool isBottomSheet04,
  }) = _HouseControlState;
  factory HouseControlState.initial() => HouseControlState(
        itemHouse: {},
        current: 1,
        size: 20,
        choosingId: "",
        processId: "",
        input: "",
        name: "",
        phone: "",
        price: "",
        remark: "",
        state: "1",
