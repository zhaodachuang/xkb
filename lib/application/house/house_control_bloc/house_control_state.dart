        stateValues: [],
        houseUnitId: [],
        // isHouseUnit: false,
        // isHousList: false,
        // isEntranceList: false,
        housList: [],
        entranceList: [],
        isBottomSheet02: false,
        isBottomSheet03: false,
        isBottomSheet04: false,
      );
}
